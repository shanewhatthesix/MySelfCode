/*

有项和无项

有向图：图的边是有方向的，只能按照箭头从一点到另一点

无向图：图的边没有方向，可以双向通行

*/

/*

权重：

在一些图中，边具有与之相关的数值，称为权重。

（权重表示从一个顶点到另一个顶点的距离/花费的代价/所需的时间/次数等 ）


*/

/*

度

有向图的几个要素：

1、入度：以当前顶点为终点的有向边的数目

2、出度：以当前顶点为起点的有向边的数目

3、度：入度 + 出度

延展：对于一个有向图，所有顶点的出度之和一定等于所有顶点的入度之和


无向图的几个要素：

度：和当前顶点相连的边的数目。

洼意：无向图没有入度和出度

*/

/*

连通：在图中从一个顶点到达另一个顶点，若存在至少一条路径，则这两个顶点是联通着的。

连通图：在无向图中，如果任意两个顶点之间能够连通，则称为无向图的连通图。

*/
