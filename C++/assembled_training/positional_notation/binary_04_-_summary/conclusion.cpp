/*

储存单位

计算机在储存数据的时候，都是按照1024做单位计算的：

*---------------------------------------------------*
|	8个二进制位(bit)    =   一个字节(byte简写B)     |
|---------------------------------------------------|
|               1024B   =   1KB                     |
|---------------------------------------------------|
|               1024KB  =   1MB                     |
|---------------------------------------------------|
|               1024MB  =   1GB                     |
|---------------------------------------------------|
|               1024GB  =   1TB                     |
|---------------------------------------------------|
|               1024TB  =   1PB                     |
*---------------------------------------------------*

注意:
	计算机的基本运算单位是位 bit
	计算机的基本储存单位是字节 byte

*/
